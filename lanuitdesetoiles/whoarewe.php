    <p>Cet évènement est organisé par les membres de la section astronomie au sein
    de l'Association Jeunes Science de Tunisie, avec la contribution des membres
    de quelques autres sections (Robotique, Informatique, Energie). Le " Mouvement
    Jeunes Science " de Tunisie est né en 1959 à l'initiative d'un groupe 
    d'intellectuels ayant constaté  l'absence d'activités scientifiques et 
    techniques parmi les associations et organisations existantes, qu'elles soient
    sportives, culturelles, ou autres . Ce fut d'abord la création d'un 
    " Club Scientifique " ouvert aux jeunes et disposant d'équipements, d'outils
    et de composants dont la manipulation leur permettait de développer leurs 
    connaissances. Ce fut, en Novembre 1967, la naissance, à Tunis, du premier club "
    Jeunes Sciences ", dans le giron de l'association des Ingénieurs et Techniciens
    Tunisiens. C'est en 1974 que fut créée l'actuelle " Association Jeunes Science
    de Tunisie " pour promouvoir les sciences et technologies et les diffuser
    auprès des Jeunes.</p>
    <p>
        L'association a pour objectifs de :
    <ul>
        <li>Inciter les jeunes à s'adonner aux activités scientifiques et techniques pendant leur temps libre.</li>
        <li>Leur donner le goût de la recherche scientifique, de la création et de l'invention technologique et éveiller en eux des vocations dans ces domaines.</li>
        <li>Oeuvrer pour une culture scientifique et technologique auprès du grand public.</li>
    </ul>
    </p>
    <p>
        La section astronomie présente une activité comportant deux parties:
    <ul>
        <li>Une partie théorique traitant les principes fondamentaux de l'astronomie à travers des exposés hebdomadaires par des animateurs volontaires, compétents, bien formés et motivés.</li> 
        <li>Une partie pratique consistant à organiser et à assister à des observations structurées et bien sécurisées offrant aux adhérents l'opportunité d'apprendre à manipuler du matériel astronomique à l'instar des lunettes et des télescopes. 
Les adhérents peuvent également proposer et concevoir des projets en rapport avec l'astronomie et l'astrophysique.</li>
    </ul>
La section astronomie organise de nombreux évènements tels que la Nuit des Etoiles, des sorties d'observation des éclipses et des éphèmerides remarquables.Elle participe également à des initiatives nationales et internationales et reçoit des astronomes, des physiciens ou des professeurs universitaires afin de présenter des conférences visant à enrichir l'activité du club.</p>
<p>
A la fin du parcours adhérent chaque individu aura la possibilité de devenir animateur après avoir participé à un stage. 
